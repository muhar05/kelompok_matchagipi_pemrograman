    private void btnEditKategoriActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditKategoriActionPerformed
        int selectedRow = TableKategori.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih kategori yang ingin diedit.");
            return;
        }

        String namaBaru = inputNameKategori.getText().trim();
        if (namaBaru.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nama kategori tidak boleh kosong.");
            return;
        }

        int idKategori = (int) TableKategori.getValueAt(selectedRow, 0); // Asumsikan kolom 0 adalah ID

        KategoriDao kategoriDao = new KategoriDao();
        boolean sukses = kategoriDao.updateKategori(idKategori, namaBaru);

        if (sukses) {
            JOptionPane.showMessageDialog(this, "Kategori berhasil diedit.");
            inputNameKategori.setText("");
            loadKategoriTable();
        } else {
            JOptionPane.showMessageDialog(this, "Gagal mengedit kategori.");
        }
    }//GEN-LAST:event_btnEditKategoriActionPerformed

    private void btnTambahKategoriActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahKategoriActionPerformed
        String namaKategori = inputNameKategori.getText().trim();
        if (namaKategori.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nama kategori tidak boleh kosong.");
            return;
        }

        KategoriDao kategoriDao = new KategoriDao();
        boolean sukses = kategoriDao.insertKategori(namaKategori);

        if (sukses) {
            JOptionPane.showMessageDialog(this, "Kategori berhasil ditambahkan.");
            inputNameKategori.setText("");
            loadKategoriTable(); // Method ini harus dibuat untuk refresh tabel
        } else {
            JOptionPane.showMessageDialog(this, "Gagal menambahkan kategori.");
        }
    }//GEN-LAST:event_btnTambahKategoriActionPerformed

    private void btnHapusKategoriActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusKategoriActionPerformed
        int selectedRow = TableKategori.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih kategori yang ingin dihapus.");
            return;
        }

        int idKategori = (int) TableKategori.getValueAt(selectedRow, 0);

        int konfirmasi = JOptionPane.showConfirmDialog(this, "Yakin ingin menghapus kategori ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
        if (konfirmasi == JOptionPane.YES_OPTION) {
            KategoriDao kategoriDao = new KategoriDao();
            boolean sukses = kategoriDao.deleteKategori(idKategori);

            if (sukses) {
                JOptionPane.showMessageDialog(this, "Kategori berhasil dihapus.");
                inputNameKategori.setText("");
                loadKategoriTable();
            } else {
                JOptionPane.showMessageDialog(this, "Gagal menghapus kategori.");
            }
        }
    }//GEN-LAST:event_btnHapusKategoriActionPerformed

