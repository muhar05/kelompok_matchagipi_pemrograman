    private void setupTableKategoriListener() {
    TableKategori.getSelectionModel().addListSelectionListener(e -> {
        if (!e.getValueIsAdjusting()) {
            int selectedRow = TableKategori.getSelectedRow();
            if (selectedRow != -1) {
                String namaKategori = TableKategori.getValueAt(selectedRow, 1).toString();
                inputNameKategori.setText(namaKategori);
            }
        }
    });
}
    
    private void loadKategoriTable() {
        KategoriDao kategoriDao = new KategoriDao();
        List<Kategori> listKategori = kategoriDao.getAllKategori();

        DefaultTableModel model = new DefaultTableModel(new String[]{"ID", "Nama Kategori"}, 0);
        for (Kategori k : listKategori) {
            model.addRow(new Object[]{k.getIdKategori(), k.getNamaKategori()});
        }

        TableKategori.setModel(model);
}


